# Q.1 Write a program using Python to obtain five numbers and print their average.
num1 = int(input("Enter 1st number\n"))
num2 = int(input("Enter 2nd number\n"))
num3 = int(input("Enter 3rd number\n"))
num4 = int(input("Enter 4th number\n"))
num5 = int(input("Enter 5th number\n"))

average = int((num1 + num2 + num3 + num4 + num5) / 5)
print("The average of the entered numbers is", average)
